                    <li><strong>Durchsetzungskraft:</strong> Sie haben sich in einer immer noch von Männern geprägten Szene durchgesetzt und sind zu Vorbildern geworden.</li>
                    <li><strong>Authentizität:</strong> Beide Künstlerinnen stehen für einen ehrlichen und leidenschaftlichen Zugang zur Musik.</li>
                    <li><strong>Community-Gedanke:</strong> Durch ihre Labels und Events fördern sie andere Künstler und tragen zur Stärkung der Techno-Kultur bei.</li>
                </ul>
